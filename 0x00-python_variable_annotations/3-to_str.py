#!/usr/bin/env python3

def to_str(n: float) -> str:
    """
    This module contains a function to_str that converts a float to its string representation.
    """

    """
        Convert a float to its string representation.

        Args:
            n (float): The float number to be converted.

        Returns:
            str: The string representation of the float number.
        """
    return str(n)
